    public DashboardFrame(String username) {
        this.username = username;
        
        setTitle("Dashboard - Pemantauan Kebijakan Publik");
        setSize(400, 300);
        setLocationRelativeTo(null);
        setDefaultCloseOperation(EXIT_ON_CLOSE);

        JLabel lblWelcome = new JLabel("Selamat datang, " + username + "!", JLabel.CENTER);
        lblWelcome.setFont(new Font("Arial", Font.BOLD, 16));

        JButton btnDataKebijakan = new JButton("ðŸ“„ Data Kebijakan");
        JButton btnLogout = new JButton("ðŸšª Logout");

        btnDataKebijakan.addActionListener(e -> {
            new crud.PolicyFrame().setVisible(true);
            // TODO: buka frame data kebijakan
        });

        btnLogout.addActionListener(e -> {
            int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin logout?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                dispose(); // tutup dashboard
                new auth.LoginFrame().setVisible(true);
            }
        });

        JPanel panel = new JPanel(new GridLayout(3, 1, 10, 10));
        panel.add(lblWelcome);
        panel.add(btnDataKebijakan);
        panel.add(btnLogout);

        add(panel, BorderLayout.CENTER);
    }
 
